def detect_emotion(text: str) -> Tuple[str, float]:
    """
    텍스트에서 감정 상태를 키워드 기반으로 감지.

    Returns:
        (mood, confidence)
        mood: 'happy' | 'excited' | 'pouty' | 'tired' | 'normal'
        confidence: 0.0 ~ 1.0
    """
    # 공백 제거 후 비교
    text_clean = text.lower().replace(' ', '')
    scores: Dict[str, int] = {mood: 0 for mood in MOOD_KEYWORDS}

    for mood, keywords in MOOD_KEYWORDS.items():
        for kw in keywords:
            if kw.replace(' ', '') in text_clean:
                scores[mood] += 1

    total = sum(scores.values())

    if total == 0:
        return 'normal', 1.0

    best_mood = max(scores, key=scores.get)

    if scores[best_mood] == 0:
        return 'normal', 1.0

    confidence = scores[best_mood] / total
    return best_mood, round(confidence, 2)


def extract_preferences(text: str) -> Dict[str, str]:
    """
    텍스트에서 취향 키워드를 추출.

    Returns:
        {'food': '중식', 'activity': '게임'} 형태
        카테고리당 첫 번째 매치만 반환
    """
    found: Dict[str, str] = {}
    text_lower = text.lower()

    for category, keywords in PREFERENCE_KEYWORDS.items():
        for kw, value in keywords.items():
            if kw in text_lower:
                found[category] = value
                break  # 카테고리당 첫 매치만

    return found
